// Sample student results (pre-filled marks for demonstration)
export const getStudentResults = (studentId: string): Subject[] => {
  const results = [...subjects];
  
  // For demonstration, generate different marks for each student
  const seed = parseInt(studentId.replace(/\D/g, '')) % 30;
  
  return results.map(subject => ({
    ...subject,
    marks: Math.min(Math.floor(Math.random() * (100 - seed) + seed), subject.maxMarks)
  }));
};